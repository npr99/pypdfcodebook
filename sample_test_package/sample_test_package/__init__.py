def add_num(a: float, b: float) -> float:
    """Return the sum of *a* and *b*.

    Examples
    --------
    >>> add_num(2, 3)
    5
    """
    return a + b


def subtract_num(a: float, b: float) -> float:
    """Return the difference of *a* and *b*.

    Examples
    --------
    >>> subtract_num(2, 3)
    -1
    """
    return a - b